import math
# round arredonda o numero para baixo e para cima - função
# ceil arredonda o numero sempre pra cima - método
# floor arredonda o numeor sempre para baixo - método
a = 5.1
b = 5.8

print('round')
print(round(a))
print(round(b))

print('ceil')
print(math.ceil(a))
print(math.ceil(b))

print('floor')
print(math.floor(a))
print(math.floor(b))
